package beans;

public class Users {

	public Integer id;
	public String login;
	public String password;
	public String role;
	
	public Users() {
		super();
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public void setRole(String role) {
		this.role = role;
	}
	
	
}
